class Math:
    a =27
    b = 3

    def __init__(self, a, b):
        self.a = a
        self.b = b

    def addition(self):
        A=self.a+self.b
        print(A)

    def multiplication(self):
        M=self.a*self.b
        print(M)

    def division(self):
        D=self.a/self.b
        print(D)

    def subtraction(self):
        S=self.a-self.b
        print(S)